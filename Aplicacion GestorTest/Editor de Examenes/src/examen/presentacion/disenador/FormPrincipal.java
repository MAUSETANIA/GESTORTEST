    private void actualizarCampos() {
        if (textPregunta.getText().trim().length() > 0 && listaRespuesta.getModel().getSize() > 0) {
            btnAdicionarPregunta.setEnabled(true);
        } else {
            btnAdicionarPregunta.setEnabled(false);
        }
    }
